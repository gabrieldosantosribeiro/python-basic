#Escreva um programa que peça ao usuário uma letra e imprima se é uma vogal ou consoante.

# Ler a letra
letra = input('digite uma letra: ').strip().lower()
#comparação
if letra == 'a':
    print('a letra é uma vogal')
elif letra == 'e':
    print('a letra é uma vogal')
elif letra == 'i':
    print('a letra é uma vogal')
elif letra == 'o':
    print('a letra é uma vogal')
elif letra == 'u':
    print('a letra é uma vogal')
else:
    print('a letra é uma consoante')